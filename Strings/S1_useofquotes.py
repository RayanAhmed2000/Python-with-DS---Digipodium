#defining strings in python
#all of the following are equivalent

my_string = 'Hello'
print(my_string)

my_string="Hello"
print(my_string)

#triple quotes string can extend multiple lines

my_string='''Hello welcome to the world 
     of Python'''
print(my_string)

my_string="""Hello, Welcome to the world
        of Python"""
print(my_string)


